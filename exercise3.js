let numberOne = Number(prompt('Number'));

let numberTwo = Number(prompt('Number'));

let operator = prompt("operator")



if (operator = "+") {
    console.log(numberOne + numberTwo); 
    
    
}else if (operator = "-") {
    console.log(numberOne - numberTwo);
}

else if (operator = "*") {
    console.log(numberOne * numberTwo);
}


else if (operator = "/") {
console.log(numberOne / numberTwo);


}

else if (operator = "*"){
    console.log(numberOne * numberTwo);
}

else{
    console.log("Invalid operator. Please use +, -, *, or /");
}


